func TestTenantService_Create_Success(t *testing.T) {
	repo := mocks.NewMockTenantRepo()
	svc := NewTenantService(repo)

	tenant, err := svc.Create(context.Background(), domain.CreateTenantRequest{
		Name: "Mon Tenant",
	})
	require.NoError(t, err)
	assert.Equal(t, "Mon Tenant", tenant.Name)
	assert.NotEmpty(t, tenant.APIKey)
	assert.True(t, tenant.IsActive)
	assert.Equal(t, "mon-tenant", tenant.Slug)
	assert.Equal(t, domain.MailPriorityDefault, tenant.Settings.DefaultPriority)
}

