func TestTenantService_List_Pagination(t *testing.T) {
	repo := mocks.NewMockTenantRepo()
	svc := NewTenantService(repo)

	for i := 0; i < 5; i++ {
		id := uuid.New()
		repo.Tenants[id] = &domain.Tenant{ID: id, Name: "T"}
	}

	list, err := svc.List(context.Background(), 1, 20)
	require.NoError(t, err)
	assert.Equal(t, int64(5), list.Total)

	// Test default pagination values
	list, err = svc.List(context.Background(), 0, 0)
	require.NoError(t, err)
	assert.Equal(t, 1, list.Page)
	assert.Equal(t, 20, list.Limit)
}
